import java.util.HashSet;

public class HashSetEx2 
{
	public static void main(String[] args) {
		
		
		HashSet hs = new HashSet<>();
		hs.add("TOPS");//0
		hs.add('H');//1
		hs.add(10);//2
		hs.add('B');//3
		hs.add('E');//4
		hs.add(10);//5
		hs.add(10);//6
		hs.add(20);//7
		
		
		
		System.out.println(hs);
		
	}
}
