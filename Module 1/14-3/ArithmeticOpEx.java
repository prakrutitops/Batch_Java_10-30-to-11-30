package com.a143;

public class ArithmeticOpEx 
{
	public static void main(String[] args) 
	{
		
		int a=6;
		int b=3;
		
		int c= a+b;
		int d= a-b;
		int e= a*b;
		int f = a/b;
		int g= a%b;
		
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}
}
